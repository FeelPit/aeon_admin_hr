    # –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö
    setup_database
    
    # –°–±–æ—Ä–∫–∞ —Ñ—Ä–æ–Ω—Ç–µ–Ω–¥–∞
    build_frontend
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
    stop_existing_processes
    
    # –ó–∞–ø—É—Å–∫ –±—ç–∫–µ–Ω–¥–∞
    start_backend
    
    # –ó–∞–ø—É—Å–∫ —Ç—É–Ω–Ω–µ–ª—è
    start_tunnel
    
    log "üéâ HR Admin System —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω–∞!"
