# –°–±–æ—Ä–∫–∞ —Ñ—Ä–æ–Ω—Ç–µ–Ω–¥–∞
build_frontend() {
    log "–°–±–æ—Ä–∫–∞ —Ñ—Ä–æ–Ω—Ç–µ–Ω–¥–∞..."
    
    cd frontend
    
    # –°–±–æ—Ä–∫–∞ –¥–ª—è –ø—Ä–æ–¥–∞–∫—à–µ–Ω–∞
    npm run build
    
    # –°–æ–∑–¥–∞–Ω–∏–µ –ø–∞–ø–∫–∏ static –≤ backend –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
    mkdir -p ../backend/static
    
    # –ö–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ —Å–æ–±—Ä–∞–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤
    cp -r build/* ../backend/static/
    
    log "‚úÖ –§—Ä–æ–Ω—Ç–µ–Ω–¥ —Å–æ–±—Ä–∞–Ω –∏ —Å–∫–æ–ø–∏—Ä–æ–≤–∞–Ω –≤ backend/static"
    cd ..
}

# –û—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
stop_existing_processes() {
    log "–û—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤..."
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ uvicorn –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
    pkill -f "uvicorn main:app" || true
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ cloudflared –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
    pkill -f "cloudflared tunnel" || true
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ react-scripts –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
    pkill -f "react-scripts" || true
    
    log "‚úÖ –°—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –ø—Ä–æ—Ü–µ—Å—Å—ã –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"
}

# –ó–∞–ø—É—Å–∫ –±—ç–∫–µ–Ω–¥–∞
start_backend() {
    log "–ó–∞–ø—É—Å–∫ –±—ç–∫–µ–Ω–¥–∞..."
    
    cd backend
    
    # –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –æ–∫—Ä—É–∂–µ–Ω–∏—è
    export DATABASE_URL="sqlite:///./hr_admin.db"
    
    # –ó–∞–ø—É—Å–∫ –≤ —Ñ–æ–Ω–æ–≤–æ–º —Ä–µ–∂–∏–º–µ
    python3 -m uvicorn main:app --host 0.0.0.0 --port 8001 &
    BACKEND_PID=$!
    
    # –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ PID –¥–ª—è –ø–æ—Å–ª–µ–¥—É—é—â–µ–π –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
    echo $BACKEND_PID > ../backend.pid
    
    # –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞
    sleep 5
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞, —á—Ç–æ —Å–µ—Ä–≤–µ—Ä –∑–∞–ø—É—Å—Ç–∏–ª—Å—è
    if curl -s http://localhost:8001/health > /dev/null; then
        log "‚úÖ –ë—ç–∫–µ–Ω–¥ –∑–∞–ø—É—â–µ–Ω –Ω–∞ http://localhost:8001"
    else
        error "–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–ø—É—Å—Ç–∏—Ç—å –±—ç–∫–µ–Ω–¥"
    fi
    
    cd ..
}

# –ó–∞–ø—É—Å–∫ Cloudflare Tunnel (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)
start_tunnel() {
    if command -v cloudflared &> /dev/null; then
        log "–ó–∞–ø—É—Å–∫ Cloudflare Tunnel..."
        
        cloudflared tunnel --url http://localhost:8001 &
        TUNNEL_PID=$!
        
        # –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ PID –¥–ª—è –ø–æ—Å–ª–µ–¥—É—é—â–µ–π –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
        echo $TUNNEL_PID > tunnel.pid
        
        # –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ —Ç—É–Ω–Ω–µ–ª—è
        sleep 10
        
        log "‚úÖ Cloudflare Tunnel –∑–∞–ø—É—â–µ–Ω"
        log "üåê –ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –±—É–¥–µ—Ç –¥–æ—Å—Ç—É–ø–Ω–æ –ø–æ URL, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–∫–∞–∂–µ—Ç Cloudflare Tunnel"
    else
        warning "cloudflared –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. –ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –¥–æ—Å—Ç—É–ø–Ω–æ —Ç–æ–ª—å–∫–æ –ª–æ–∫–∞–ª—å–Ω–æ –Ω–∞ http://localhost:8001"
    fi
}

# –§—É–Ω–∫—Ü–∏—è –æ—á–∏—Å—Ç–∫–∏ –ø—Ä–∏ –≤—ã—Ö–æ–¥–µ
cleanup() {
    log "–û—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è..."
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –±—ç–∫–µ–Ω–¥–∞
    if [ -f "backend.pid" ]; then
        kill $(cat backend.pid) 2>/dev/null || true
        rm -f backend.pid
    fi
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ —Ç—É–Ω–Ω–µ–ª—è
    if [ -f "tunnel.pid" ]; then
        kill $(cat tunnel.pid) 2>/dev/null || true
        rm -f tunnel.pid
    fi
    
    # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –≤—Å–µ—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
    pkill -f "uvicorn main:app" || true
    pkill -f "cloudflared tunnel" || true
    
    log "‚úÖ –ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ"
}

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞ —Å–∏–≥–Ω–∞–ª–æ–≤
trap cleanup EXIT INT TERM

